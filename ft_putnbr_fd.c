#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long x;

	x = n;
	if (x < 0)
	{
		ft_putchar_fd('-', fd);
		x = x * -1;
	}
	if ((x / 10) > 0)
		ft_putnbr_fd((x / 10), fd);
	ft_putchar_fd((x % 10 + 48), fd);
}
